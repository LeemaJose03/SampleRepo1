package oopsConcept;

public class Encapsulation {

	 
	int rollno;
	String name;
	
	
	public void setRollno(int rollno) {  
		this.rollno = rollno;  
	}
	
	public int getRollno() {
	
		return rollno;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	

}
